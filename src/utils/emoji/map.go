var str2img map[string]string = make(map[string]string)

func runes2hex(src []rune) string {
	ret := make([]string, 0)

	for _, char := range src {
		ret = append(ret, fmt.Sprintf(`%x`, char))
	}

	return strings.Replace(strings.Join(ret, `-`), `-fe0f`, ``, -1)
}

func init() {
	for _, chars := range name2codes {
		str := string(chars)
		str2img[str] = strings.Join([]string{`//twemoji.maxcdn.com/svg/`, runes2hex(chars), `.svg`}, ``)
	}
}
